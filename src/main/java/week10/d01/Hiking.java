package week10.d01;

public class Hiking {

    private double ascend;
    private double descend;

    public Hiking(double ascend, double descend) {
        this.ascend = ascend;
        this.descend = descend;
    }

    public double getAscend() {
        return ascend;
    }

    public double getDescend() {
        return descend;
    }
}
