package week10.d01;

public class Hiking {

    private int ascend;
    private int descend;

    public Hiking(int ascend, int descend) {
        this.ascend = ascend;
        this.descend = descend;
    }

    public int getAscend() {
        return ascend;
    }

    public int getDescend() {
        return descend;
    }
}
